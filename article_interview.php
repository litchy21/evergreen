                    <h4>• Comment vous est venue l’idée de ce film ?</h4>
                        <p>
                            C’est une question qui m’est souvent posée. Pour certains de mes scenarii je me souviens du moment où l’idée m’est venue, mais pas tout le temps. Concernant Evergreen je n'ai aucune idée. C’était peut-être en lisant la phrase d’Einstein: “Je ne sais pas comment sera la troisième guerre mondiale, mais je sais qu'il n'y aura plus beaucoup de monde pour voir la quatrième."
                        </p>

                    <h4>• Quel message vouliez vous passer?</h4>
                        <p>
                            Il faut arreter toutes les guerres tout de suite.
                        </p> 

                    <h4>• Diriez-vous que c’est optimiste ou pessimiste?</h4>
                        <p>
                            Je ne sais pas. Je dirais réaliste.
                        </p>

                    <h4>• Combien de temps entre l’idée et le film sur un écran?</h4>
                        <p>
                            Pour Evergreen c’était un an. Après avoir été rejetée par le Centre National du Cinema (CNC) pour financer le film j’ai crée un site de “crowdfunding” et j’ai pu tourner le film avec des donations de la famille, d'amis et d'inconnus. J’ai eu beaucoup de chance d’avoir ces gens pour me soutenir.
                        </p>

                    <h4>• Avez-vous choisit les acteurs?</h4>
                        <p>
                            Oui je les ai choisis. Comme j’étais à Paris j’ai fais un casting par Skype et un autre par un site de casting. Ca a bien marché! Je crois. 
                        </p>

                    <h4>• La musique a obtenu un prix dès la première projection, avez-vous dirigé le musicien?</h4>
                        <p>
                            Oui. Je lui envoyé des samples, et j’ai décidé pour les passages où je voulais avoir du silence. Mais les idées que Constantinos a apportées étaient au-delà de mes espérances.
                        </p>

                    <h4>• En conclusion: C’était votre première réalisation. Que retenez vous de positif?</h4>
                        <p>
                            C’est un métier pour moi. J’ai adoré chaque seconde, de la préparation au montage.
                        </p>

                    <h4>• Et de négatif?</h4>
                        <p>
                            Rien. Les quelques petits problèmes qu’on a eus, m’ont aidée à ne pas refaire les mêmes erreurs la fois suivante.
                        </p>
